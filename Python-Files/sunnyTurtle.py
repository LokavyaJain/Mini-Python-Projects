def star():
    colours = ["red", "medium blue", "green", "blue violet"]
    s = turtle.Turtle()
    s.penup()
    s.speed(1000)
    for x in range (0, random.randint(10, 15)):
        s.penup()
        coordinatex1 = (random.randint(-500,0))
        coordinatey = (random.randint(-300,300 ))
        s.goto(coordinatex1, coordinatey)
        s.pensize(3)
        s.pendown()
        s.color(random.choice(colours))
        for y in range(0, 3):
            s.pendown
            for z in range (2):
                s.right(90)
                for a in range (18):
                    s.fd(2)
                    s.rt(3)
        s.penup()
        coordinatex2 = (random.randint(0, 500))
        coordinatey = (random.randint(-300, 300))
        s.goto(coordinatex2, coordinatey)
        s.pensize(3)
        s.pendown()
        s.color(random.choice(colours))
        for y in range(0, 3):
            s.pendown
            for z in range (2):
                s.right(90)
