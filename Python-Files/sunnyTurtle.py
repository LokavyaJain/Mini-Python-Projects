def flower():
    f = turtle.Turtle()
    f.penup()
    f.setposition(turtle.pos())
    f.penup()
    colours = ["tomato", "cyan", "green yellow", "yellow", "orange red", "lavender", "light pink"]
    colour = random.choice(colours)
    f.pen(pencolor = "black", fillcolor = colour)
    f.penup()
    f.shape("circle")
    f.shapesize(3, 3/2, 4)
    f.penup()
    f.stamp()
    f.penup()
    f.rt(90)
