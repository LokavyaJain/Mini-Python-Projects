print("This program will do some simple math with two numbers")
print("")
print("")

firstNum = float(input("Give me a number "))
print("")
print("")
secondNum = float(input("Give me another number "))
print("")
print("")

print(firstNum, "+", secondNum, "=", firstNum + secondNum)
print(firstNum, "-", secondNum, "=", firstNum - secondNum)
print(firstNum, "x", secondNum, "=", firstNum * secondNum)
print(firstNum, "/", secondNum, "=", firstNum / secondNum)
print(firstNum, "%", secondNum, "=", firstNum % secondNum)
print(firstNum, "^", secondNum, "=", firstNum ** secondNum)

print("")
print('')

input("Press enter to finish.")
